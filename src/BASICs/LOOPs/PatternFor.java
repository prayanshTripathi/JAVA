package BASICs.LOOPs;

public class PatternFor {
    public static void main(String[] args) {
        // pattern in Java
        // Patterns Using For

        for (int i=0; i<40; i++){
            for (int j=0; j<=i; j++){
                System.out.print(" *");
            }
            System.out.println();
        }



    }
}
