package basics.operators;

public class Relational {
    public static void main(String[] args) {
        // Relational Operator
        // ==, !=, >=, <=, >, <


        int a=5,b=6;
        System.out.println(a!=b);// Is a is not equal to b ? --> True
        System.out.println(a==b);// Is a is equal to b ? --> False 
        System.out.println(a>=b);// Is a is greater than or equal to b ? --> False
        System.out.println(a<=b);// Is a is smaller than or equal to b ? --> True
        System.out.println(a>b);// Is a is greater than b ? --> False
        System.out.println(a<b);// Is a is smaller than b ? --> True
        
    }
}
